public class FiveToTwenty
{
    public static void main(String [] args)
    {

        int i = 5; // Starting value
        while(i <= 20) // Keep looping as long as some condition is true
        {
            System.out.print(i + " ");
            i++; // Modify i
        }
       
        System.out.println(); // Finish with a new line.
    }
}