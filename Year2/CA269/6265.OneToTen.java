public class OneToTen
{
    public static void main(String [] args)
    {

        int i = 1; // Starting value
        while(i <= 10) // Keep looping as long as some condition is true
        {
            System.out.print(i + " ");
            i++; // Modify i
        }
       
        System.out.println(); // Finish with a new line.
    }
}